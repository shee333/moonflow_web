import { Workflow, WorkflowNode, WorkflowEdge } from '../components/types';

export interface CodeGeneratorOptions {
  asyncExecution?: boolean;
  errorHandling?: boolean;
  logging?: boolean;
  typeAnnotations?: boolean;
}

export function generateMoonBitCode(
  workflow: Workflow,
  options: CodeGeneratorOptions = {}
): string {
  const {
    asyncExecution = true,
    errorHandling = true,
    logging = true,
    typeAnnotations = true,
  } = options;

  const { name, description, nodes, edges } = workflow;
  
  const lines: string[] = [
    `// MoonFlow Workflow: ${name}`,
    `// Generated by MoonFlow Studio v0.1.0`,
    `// ${new Date().toISOString()}`,
    '',
    'package moonflow_generated',
    '',
  ];

  if (logging) {
    lines.push('import moonflow/core { Logger, LogLevel }');
    lines.push('');
  }

  if (asyncExecution) {
    lines.push('import moonflow/async { AsyncExecutor, Pipeline }');
    lines.push('');
  }

  lines.push('pub fn main {');
  lines.push(`  println("Starting workflow: ${name}")`);
  
  if (logging) {
    lines.push('  Logger.info("Initializing workflow execution")');
  }

  lines.push('');
  lines.push('  // Initialize workflow context');
  lines.push('  let ctx = WorkflowContext::()');
  lines.push('');

  lines.push('  // Initialize and configure nodes');
  nodes.forEach((node, index) => {
    const varName = toVariableName(node.label) + '_' + index;
    lines.push(`  let ${varName} = ${generateNodeInit(node, typeAnnotations)}`);
    
    if (node.config && Object.keys(node.config).length > 0) {
      lines.push(`  ${varName}.configure(${generateConfigCode(node)})`);
    }
  });

  lines.push('');
  lines.push('  // Build execution pipeline');
  
  if (asyncExecution) {
    lines.push('  let pipeline = Pipeline::()');
    const sortedNodes = topologicalSort(nodes, edges);
    sortedNodes.forEach(nodeId => {
      const node = nodes.find(n => n.id === nodeId)!;
      const varName = toVariableName(node.label) + '_' + nodes.indexOf(node);
      lines.push(`  pipeline.add(${varName})`);
    });
    lines.push('');
    lines.push('  // Execute pipeline asynchronously');
    lines.push('  let result = pipeline.execute(ctx)');
  } else {
    const startNodes = findStartNodes(nodes, edges);
    startNodes.forEach((node) => {
      const varName = toVariableName(node.label) + '_' + nodes.indexOf(node);
      lines.push(`  execute_node(${varName}, ctx)`);
    });
  }

  lines.push('');
  
  if (errorHandling) {
    lines.push('  // Handle execution results');
    lines.push('  match result {');
    lines.push('    Ok(_) => {');
    if (logging) {
      lines.push('      Logger.info("Workflow completed successfully")');
    }
    lines.push('      println("Workflow execution completed")');
    lines.push('    }');
    lines.push('    Err(e) => {');
    if (logging) {
      lines.push('      Logger.error(`Workflow failed: $\{e.message}`)');
    }
    lines.push('      println(`Error: $\{e.message}`)');
    lines.push('    }');
    lines.push('  }');
  } else {
    if (logging) {
      lines.push('  Logger.info("Workflow execution completed")');
    }
    lines.push('  println("Workflow execution completed")');
  }

  lines.push('}');
  lines.push('');
  lines.push('// Workflow Context');
  lines.push('struct WorkflowContext {');
  lines.push('  state: Map[String, Any]');
  lines.push('  metadata: Map[String, String]');
  lines.push('}');
  lines.push('');
  lines.push('impl WorkflowContext {');
  lines.push('  fn init() -> WorkflowContext {');
  lines.push('    WorkflowContext::{ state: Map::(), metadata: Map::() }');
  lines.push('  }');
  lines.push('');
  lines.push('  fn get(self, key: String) -> Option[Any] {');
  lines.push('    self.state.get(key)');
  lines.push('  }');
  lines.push('');
  lines.push('  fn set(self, key: String, value: Any) -> () {');
  lines.push('    self.state.set(key, value)');
  lines.push('  }');
  lines.push('');
  lines.push('  fn clear(self) -> () {');
  lines.push('    self.state.clear()');
  lines.push('  }');
  lines.push('}');
  lines.push('');
  lines.push('// Node type definitions');
  lines.push(generateNodeTypeDefinitions());
  lines.push('');
  lines.push('// Node factory functions');
  lines.push(generateNodeFactoryFunctions());
  lines.push('');
  lines.push('// Execution functions');
  lines.push(generateExecutionFunctions(errorHandling, logging));
  lines.push('');
  lines.push('// Helper functions');
  lines.push(generateHelperFunctions(edges, nodes));

  return lines.join('\n');
}

function generateNodeInit(node: WorkflowNode, typeAnnotations: boolean): string {
  const componentType = getComponentType(node.type);
  const typeHint = typeAnnotations ? `: ${componentType}` : '';
  return `init_${node.type}_node("${node.label}"${typeHint})`;
}

function getComponentType(type: string): string {
  const typeMap: Record<string, string> = {
    http: 'HttpNode',
    llm: 'LlmNode',
    database: 'DatabaseNode',
    cache: 'CacheNode',
    response: 'ResponseNode',
    transformer: 'TransformerNode',
    websocket: 'WebSocketNode',
    grpc: 'GrpcNode',
    graphql: 'GraphQLNode',
  };
  return typeMap[type] || 'GenericNode';
}

function generateConfigCode(node: WorkflowNode): string {
  if (!node.config) return '{}';
  
  const configPairs = Object.entries(node.config)
    .map(([key, value]) => `${key}: ${JSON.stringify(value)}`)
    .join(', ');
  
  return `{ ${configPairs} }`;
}

